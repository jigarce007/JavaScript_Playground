//const for constant value ... can not be change in future once asigned the value.
const birthyear = 1991

//var is similar to 'let' but there is differnce of "function specific and block specific"
var lName;
lName = "Makwana"

console.log(fName)
console.log(lName)
console.log(birthyear)


